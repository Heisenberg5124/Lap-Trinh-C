#include <stdio.h>

int main ()
{
	int m=1,a,b,n,c;
	printf("Hay nhap so nguyen a:   ");
	scanf("%d",&a);
	printf("Hay nhap so nguyen b:   ");
	scanf("%d",&b);
	
	for(m=1 ; m<=a,b ; m++)
	{
		

		if (m%a==0&&m%b==0)
		{
			printf ("Boi chung nho nhat la : %d",m);
			break;
		}			
	
	}
	
	for( ; a>=c ; )
		{
		c=a;
		n=c--;
		
		
		if (a%n==0&&b%n==0)
		{
			
			printf("Uoc so chung lon nhat la : %d",n);
			break;
		}
}
	
}


